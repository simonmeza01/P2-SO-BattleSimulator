    private void updateQueue(javax.swing.JTextArea ta, Queue q){
        ta.setText("");
        if (q.isEmpty()){return;}
        Personaje head = q.getHead();
        ta.append(head.id);
        head = head.getNext();
        while (head!=null){
            ta.append("  -  " + head.id);
            head = head.getNext();
        }
    }
    
