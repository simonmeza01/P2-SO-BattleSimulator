    public void updateQueues(Queue AV1, Queue AV2, Queue AV3, Queue AV4, Queue US1, Queue US2, Queue US3, Queue US4){
        this.updateQueue(taSF1, US1);
        this.updateQueue(taSF2, US2);
        this.updateQueue(taSF3, US3);
        this.updateQueue(taSF4, US4);
        
        this.updateQueue(taZE1, AV1);
        this.updateQueue(taZE2, AV2);
        this.updateQueue(taZE3, AV3);
        this.updateQueue(taZE4, AV4);

    }
    
    private void updateQueue(javax.swing.JTextArea ta, Queue q){
        ta.setText("");
        if (q.isEmpty()){return;}
        Personaje head = q.getHead();
        ta.append(head.id);
        head = head.getNext();
        while (head!=null){
            ta.append("  -  " + head.id);
            head = head.getNext();
        }
    }
    
